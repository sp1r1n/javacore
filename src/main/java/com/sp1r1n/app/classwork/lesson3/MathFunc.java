public class MathFunc {

    public int multiply (int a, int b) {return a * b;}
    public int diff (int a, int b) {return a - b;}
    public int sum (int a, int b) {return a + b;}
    public int div (int a, int b) {return a / b;}
    public double sqrt (double x) {return Math.sqrt(x);}
    public double pow (double x, double y) {return Math.pow(x, y);}
    public float percentage (float n, float m) {return ((n*100)/m);}
    public int increment (int a) {return ++a;}
    public int decrement (int a) {return --a;}
    public double sin (double x) {return Math.sin(x);}
    public double acos (double x) {return Math.acos(x);}
    public float round (float n) {return Math.round(n);}
    public double abs (double x) {return Math.abs(x);}
    public int changeSign (int a) {return a * -1;}
}

